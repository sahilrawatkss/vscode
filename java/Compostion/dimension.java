package Compostion;

public class dimension {
    private int length;
    private int breadth;
    private int depth;

    public dimension(int length, int breadth, int depth) {
        this.length = length;
        this.breadth = breadth;
        this.depth = depth;
    }

    public int getLength() {
        return length;
    }

    public int getBreadth() {
        return breadth;
    }

    public int getDepth() {
        return depth;
    }
}
